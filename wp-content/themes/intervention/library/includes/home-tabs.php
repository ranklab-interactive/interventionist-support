		<div class="tab-why">
		<p>When a friend or family member has a behavioral health disorder or a dependency on a controlled substance, he or she may never actively seek treatment for the addiction or condition. This can be attributed to a number of factors. Find out if they need a drug intervention</p>
		<h2>A loved one is in need of an intervention if they:</h2>
	
		<div class="icon-item checkbox pos1">
			<div class="text-block">
		   		<p>Don’t recognize they have a problem requiring treatment or rehabilitation.</p>
			</div><!-- end text-block -->
	   	</div><!-- end icon-item -->
	   	<div class="icon-item checkbox pos2">
			<div class="text-block">
		   		<p>Are uninformed about how their condition can be detrimental to their overall health.</p>
			</div><!-- end text-block -->
	   	</div><!-- end icon-item -->
	   	<div class="icon-item checkbox pos3">
			<div class="text-block">
		   		<p>Fail to understand how it negatively affects their relationships with friends and family.</p>
			</div><!-- end text-block -->
	   	</div><!-- end icon-item -->
	   	<div class="icon-item checkbox pos4">
			<div class="text-block">
		   		<p>Feel they are not capable of receiving successful treatment for their addiction or mental illness.</p>
			</div><!-- end text-block -->
	   	</div><!-- end icon-item -->
	
<p class="pos-copy">Holding a drug or alcohol intervention can be an eye opening experience and bring to light the depths of one's problem. <strong>Done correctly, an intervention can persuade one to seek treatment.</strong></p>
