	<div class="two dynamic" style="display:none">
		<div class="tab-signs">
	   <p>There’s a fine line of when use becomes abuse and areas of gray when it comes to needing treatment for a mental health issue. As a caring friend or family member, it’s vital that you learn to recognize the signs and symptoms of a potential problem before it gets out of hand.</p>
	   
	   	<div class="icon-item round-arrow">
			<div class="text-block">
				<h2>Drug Addiction Signs</h2>
		   		<p>Every type of drug addiction displays its own unique signs and symptoms but most share some of the following:</p> 
		   		<ul>
		   			<li>When You Find Drug Paraphernalia</li>
		   			<li>A Change in Acquaintances</li>
		   			<li>Displaying Behaviors out the Norm</li>
		   			<li>A Deteriorating Appearance</li>
		   			<li>A Change in their Outlook in Life</li>
		   		</ul>
			</div><!-- end text-block -->
	   	</div><!-- end icon-item -->
	   	<div class="icon-item round-arrow">
			<div class="text-block">
				<h2>Behavioral Problems</h2>
		   		<p>Behavioral problems can be classified as any process addiction that affects one’s daily life or the lives of those around them. To name just a few, these can include:</p>
		   		<ul>
		   			<li>Eating Disorders</li>
		   			<li>Problem Gambling</li>
		   			<li>Sex Addiction</li>
		   			<li>Obsessive Compulsive Disorders</li>
		   		</ul>
			</div><!-- end text-block -->
	   	</div><!-- end icon-item -->
	   	<div class="icon-item round-arrow">
			<div class="text-block">
				<h2>Mental Illness</h2>
		   		<p>Mental health related issues are often associated with a co-occurring substance abuse problem. Several disorders that might require an interventionist include:</p>
		   		<ul>
		   			<li>Severe Deppresion</li>
		   			<li>Post Traumatic Stress </li>
		   			<li>Anxiety and  Panic</li>
		   			<li>Anger Management</li>
		   			<li>Attention Deficit Disorder</li>
		   		</ul>
			</div><!-- end text-block -->
	   	</div><!-- end icon-item -->

<p class="left">If you observe any signs of addiction or symptoms of mental illness in a loved one, it’s never too early to reach out to them through an intervention - and rarely is it ever too late either.</p>
