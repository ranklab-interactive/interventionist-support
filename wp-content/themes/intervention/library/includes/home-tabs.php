	<div class="three dynamic" style="display:none">
		<div class="tab-types">
	   	<p>An intervention is much more than just sitting down as a group to confront an addict; it requires a strategy and a plan of action in order for it to be successful. The right intervention technique will depend on the individual addict’s personality and current situation. Consider what method might work best for your loved one:</p>
		<div class="icon-item round-plus">
			<div class="text-block">
		   		<p>Give them the tough love approach by threatening to cut them off financially or seeking legal action against them.</p>
			</div><!-- end text-block -->
	   	</div><!-- end icon-item -->
	   	<div class="icon-item round-plus">
			<div class="text-block">
		   		<p>Try the "love first" method to show the addict how much you care by reading personal letters to him or her.</p>
			</div><!-- end text-block -->
	   	</div><!-- end icon-item -->
	   	<div class="icon-item round-plus">
			<div class="text-block">
		   		<p>Use a model that keeps things positive and provides the addict with options in the way of his or her treatment.</p>
			</div><!-- end text-block -->
	   	</div><!-- end icon-item -->

	<p>Planning is the key to any successful intervention, so it’s important to understand exactly what each model involves and how it would go over for all the parties involved.</p>
